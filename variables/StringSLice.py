b = '0123456789'
print('b[1 : 10 : 3] = ', b[1 : 10 : 3])
print('b[1 : 10 : 2] = ', b[1 : 10 : 2])
print('b[10 : 1 : -1] = ', b[10 : 1 :-1])
print('b[ : : -1] = ', b[ : :-1])
print('b[2 : 8 : -1] = ', b[2 : 8 :-1])
print('b[-1 : -6 : -1] = ', b[-1 :-6 :-1])
print('b[2 : -5 : 1] = ', b[2 :-5 : 1])
